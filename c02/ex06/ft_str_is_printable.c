int	ft_str_is_printable(char *str)
{
	int	n;
	int	i;

	n = 1;
	i = 0;
	while (n != 0)
	{
		n = *str + i;
		i++;
		if (n < 32 && n > 0 || n == 127)
			return (0);
	}
	return (1);
}
