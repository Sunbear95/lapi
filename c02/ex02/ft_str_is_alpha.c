int	ft_str_is_alpha(char *str)
{
	int	a;
	int	i;

	a = 0;
	while (a != 0)
	{
		a = str[i];
		if (a < 64 || (a > 91 && a < 96))
			return (0);
		else if (a > 123)
			return (0);
		i++;
	}
	return (1);
}
