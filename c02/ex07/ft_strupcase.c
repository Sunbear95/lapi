char	*ft_strupcase(char *str)
{
	int	i;
	int	n;

	n = 1;
	i = 0;
	while (n != 0)
	{
		n = str[i];
		if (n > 96 && n < 123)
		{
			str[i] = n - 32;
		}
		i++;
	}
	return (str);
}
