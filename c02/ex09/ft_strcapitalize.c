char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i + 1] != 0)
	{
		if (str[0] < 123 && str[0] > 96)
			str[0] -= 32;
		else if ((str[i] == ' ' ||str[i] == '-' || str[i] == '+')
				&& (str[i + 1] < 123 && str[i + 1] > 96))
			str[i + 1] -= 32;
		while (((str[i] < 91 && str[i] > 64) || (str[i] < 123 && str[i] > 96))
			&& (str[i + 1] < 91 && str[i + 1] > 64))
		{
			str[i + 1] += 32;
			i++;
		}
		i++;
	}
	return (str);
}
