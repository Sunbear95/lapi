#include "libft.h"

int	ft_isalpha(char *str)
{
	int	i;

	i = -1;
	while (str[i] != 0)
	{
		if (str[i] < 'a' || (str[i] < 'A' && str[i] > 'z') || str[i] > 'Z')
			return (0);
		i++;
	}
	return (1);
}
