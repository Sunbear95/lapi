#include "libft.h"

int	ft_isalnum(char *str)
{
	int		i;

	i = 0;
	while (str[i] != 0)
	{
		if (!((str[i] >= '0' && str[i] <= '9')
				|| (str[i] >= 'a' && str[i] <= 'z'
					|| str[i] >= 'A' && str[i] <= 'Z')))
			return (1);
		i++;
	}
	return (0);
}
