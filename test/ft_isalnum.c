#include "libft.h"

int	ft_isalnum(int num)
{
	if (!((num >= '0' && num <= '9')
			|| (num >= 'a' && num <= 'z')
				|| (num >= 'A' && num <= 'Z')))
		return (1);
	return (0);
}
