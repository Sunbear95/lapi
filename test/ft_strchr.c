#include "libft.h"
#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] != c)
		i++;
	if (s[i] == 0)
		return (0);
	return ((char *)(s + i));
}
