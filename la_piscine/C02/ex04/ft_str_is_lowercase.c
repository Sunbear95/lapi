int	ft_str_is_lowercase(char *str)
{
	int	a;

	a = 1;
	while (a != 0)
	{
		a = *str;
		if (a == 0)
			return (1);
		if (a < 97 || a > 122)
			return (0);
		str++;
	}
	return (1);
}
