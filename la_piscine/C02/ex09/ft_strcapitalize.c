char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] <= 'Z' && str[i] >= 'A')
			str [i] += 32;
		i++;
	}
	i = 0;
	while (str[i + 1] != 0)
	{
		if (str[0] <= 'z' && str[0] >= 'a')
			str[0] -= 32;
		if ((str[i] < '0' || (str[i] > '9' && str[i] < 'A')
				|| (str[i] > 'Z' && str[i] < 'a') || str[i] > 'z')
			&& (str[i + 1] <= 'z' && str[i + 1] >= 'a'))
			str[i + 1] -= 32;
		i++;
	}
	return (str);
}
