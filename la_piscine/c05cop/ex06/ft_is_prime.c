#include <stdbool.h>

int	ft_is_prime(int nb)
{
	int	f;

	if (nb <= 1)
		return (false);
	f = 2;
	while (f * f <= nb)
	{
		if (nb % f == 0)
			return (false);
		f++;
	}
	return (true);
}
