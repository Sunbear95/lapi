#include <unistd.h>

void	ft_is_negative(int n)
{
	char	min;
	char	plus0;

	min = 'N';
	plus0 = 'P';
	if (n < 0)
	{
		write (1, &min, 1);
	}
	else
	{
		write (1, &plus0, 1);
	}
}	
